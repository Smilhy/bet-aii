const POLISH_HINT_RE = /[ąćęłńóśźżĄĆĘŁŃÓŚŹŻ]|\b(?:brak|typ(?:y|ów)?|mecz(?:e|ów)?|kurs(?:y|ów)?|wynik(?:i|ów)?|użytkownik(?:a|ów)?|typer(?:a|ów)?|zaloguj|wyloguj|dodaj|usuń|edytuj|zapisz|wybierz|pokaż|zobacz|kup|sprzedaj|dzisiaj|dzis|wczoraj|jutro|powiadomienia|płatności|wypłaty|wpłaty|zarobki|subskrypcja|polecenia|społeczność|artykuły|nagrody|ustawienia)\b/i

function replaceCaseInsensitiveWhole(source, from, to) {
  const escaped = from.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  return source.replace(new RegExp(`(^|[^\\p{L}\\p{N}_])(${escaped})(?=$|[^\\p{L}\\p{N}_])`, 'giu'), (match, prefix, found) => {
    let replacement = to
    if (found === found.toUpperCase() && /[A-ZĄĆĘŁŃÓŚŹŻ]/.test(found)) replacement = to.toUpperCase()
    else if (found[0] === found[0]?.toUpperCase()) replacement = to.charAt(0).toUpperCase() + to.slice(1)
    return prefix + replacement
  })
}

function applyDynamicPatterns(text) {
  return text
    .replace(/Pokazano\s+(\d+)\s+z\s+(\d+)\s+typów/gi, 'Showing $1 of $2 picks')
    .replace(/Pokaż kolejne\s+(\d+)\s+typy?\s*\((\d+)\s+pozostało\)/gi, 'Show $1 more picks ($2 remaining)')
    .replace(/Pokaż kolejne\s+(\d+)\s+typy?/gi, 'Show $1 more picks')
    .replace(/Zwiń do\s+(\d+)\s+typów/gi, 'Collapse to $1 picks')
    .replace(/(\d+)\s+wiadomości dziś/gi, '$1 messages today')
    .replace(/(\d+)\s+wiadomości/gi, '$1 messages')
    .replace(/(\d+)\s+typów dziennie/gi, '$1 picks per day')
    .replace(/Powyżej\s+([0-9.,]+)\s+gola?/gi, 'Over $1 goals')
    .replace(/Poniżej\s+([0-9.,]+)\s+gola?/gi, 'Under $1 goals')
    .replace(/Powyżej\s+([0-9.,]+)\s+pkt/gi, 'Over $1 points')
    .replace(/Poniżej\s+([0-9.,]+)\s+pkt/gi, 'Under $1 points')
    .replace(/Powyżej\s+([0-9.,]+)\s+runów/gi, 'Over $1 runs')
    .replace(/Poniżej\s+([0-9.,]+)\s+runów/gi, 'Under $1 runs')
    .replace(/Powyżej\s+([0-9.,]+)\s+rożnych/gi, 'Over $1 corners')
    .replace(/Poniżej\s+([0-9.,]+)\s+rożnych/gi, 'Under $1 corners')
    .replace(/\b([0-9]+(?:[.,][0-9]+)?)\s*zł\b/gi, '$1 PLN')
    .replace(/\b0 zł\b/gi, '0 PLN')
    .replace(/\b(\d+)\s+dni\b/gi, '$1 days')
    .replace(/\b(\d+)\s+godz\.\s+temu\b/gi, '$1h ago')
    .replace(/\b(\d+)\s+min\s+temu\b/gi, '$1 min ago')
}

export function translateBetaiEnglishText(value) {
  if (value === null || value === undefined) return value
  const raw = String(value)
  const trimmed = raw.trim()
  if (!trimmed) return value

  const exact = EN_EXACT[trimmed]
  if (exact) return raw.replace(trimmed, exact)

  // Do not touch values that have no Polish signal. This keeps names, teams,
  // IDs and already-English copy intact.
  if (!POLISH_HINT_RE.test(trimmed)) return value

  let next = applyDynamicPatterns(trimmed)
  for (const [from, to] of EN_PHRASES) {
    next = replaceCaseInsensitiveWhole(next, from, to)
  }

  // Final punctuation and a few common grammar cleanups after word fallback.
  next = next
    .replace(/\bNo picks tipsters\b/gi, 'No tipster picks')
    .replace(/\bNo data for today\b/gi, 'No data for today')
    .replace(/\bYour balance:\s*/gi, 'Your balance: ')
    .replace(/\bCoinsów\b/gi, 'Coins')
    .replace(/\bCoinów\b/gi, 'Coins')
    .replace(/\bPLN PLN\b/g, 'PLN')
    .replace(/\s{2,}/g, ' ')

  return next !== trimmed ? raw.replace(trimmed, next) : value
}

